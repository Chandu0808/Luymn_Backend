def perform_lap_handshake(processor, private_key, csr):
    """Perform LAP certificate exchange with processor"""
    
    print_header("LAP Certificate Handshake")
    
    print_info(f"Connecting to {processor['server']} ({processor['ipv4']}) on port {LAP_PORT}...")
    
    try:
        # Setup SSL context with LAP certificates
        context = ssl.SSLContext(ssl.PROTOCOL_TLS_CLIENT)
        context.verify_mode = ssl.CERT_REQUIRED
        context.load_verify_locations(cafile=LAP_LUTRON_CHAIN_FILE)
        context.load_cert_chain(
            certfile=LAP_SIGNED_CSR_FILE,
            keyfile=LAP_PRIVATE_KEY_FILE
        )
        context.check_hostname = False
        
        # Connect to processor
        sock = socket.create_connection((processor['ipv4'], LAP_PORT), timeout=10)
        ssock = context.wrap_socket(sock)
        sock.close()
        
        print_success(f"Connected to processor on LAP port {LAP_PORT}")
        
        # Step 1: Send Ping
        print_info("Sending Ping request...")
        send_json(ssock, {
            "Header": {
                "RequestType": "Ping",
                "ClientTag": "ping"
            }
        })
        
        recv_msg = recv_json(ssock)
        if not recv_msg or recv_msg["Header"]["StatusCode"] != "204 No Content":
            if recv_msg is None:
                print_error("Processor closed connection. Has the processor been set up with Lutron's tools?")
            else:
                print_error(f"Ping failed: {recv_msg.get('Header', {}).get('StatusCode', 'Unknown error')}")
            return None
        
        print_success("Ping successful")
        
        # Step 2: Wait for physical button press
        print_header("Physical Access Verification Required")
        print_warning(f"Please press the button on processor {processor['serial']}")
        print_info("Button location varies by device (usually on front panel or inside)")
        print_info(f"Waiting for button press (timeout: {BUTTON_PRESS_TIMEOUT}s)...")
        print(f"{Colors.BOLD}Press the button NOW...{Colors.ENDC}")
        
        recv_msg = recv_json(ssock, timeout=BUTTON_PRESS_TIMEOUT)
        if not recv_msg or recv_msg["Header"]["StatusCode"] != "200 OK":
            print_error("Button press not detected or timeout occurred")
            return None
        
        print_success("Button press detected! Physical access verified")
        
        # Step 3: Retrieve processor's root CA certificate
        print_info("Retrieving processor's root CA certificate...")
        send_json(ssock, {
            "Header": {
                "RequestType": "Read",
                "Url": "/certificate/root",
                "ClientTag": "read-root",
            },
        })
        
        root_resp = recv_json(ssock)
        if not root_resp or root_resp["Header"]["StatusCode"] != "200 OK":
            print_error("Failed to retrieve processor root certificate")
            return None
        
        proc_root_cert = root_resp["Body"]["Certificate"]["Certificate"]
        print_success("Processor root certificate received")
        
        # Step 4: Send CSR for signing
        print_info("Sending CSR to processor for signing...")
        send_json(ssock, {
            "Header": {
                "RequestType": "Execute",
                "Url": "/pair",
                "ClientTag": "get-cert",
            },
            "Body": {
                "CommandType": "CSR",
                "Parameters": {
                    "CSR": csr.public_bytes(serialization.Encoding.PEM).decode('ASCII'),
                    "DisplayName": DEVICE_COMMON_NAME,
                    "DeviceUID": "000000000000"
                },
            },
        })
        
        recv_msg = recv_json(ssock)
        if not recv_msg or recv_msg["Header"]["StatusCode"] != "200 OK":
            print_error("CSR signing failed")
            return None
        
        print_success("CSR signed successfully by processor")
        
        # Extract certificates from response
        signed_csr = recv_msg["Body"]["SigningResult"]["Certificate"]
        proc_intermediate_cert = recv_msg["Body"]["SigningResult"]["RootCertificate"]
        private_key_pem = private_key.private_bytes(
            serialization.Encoding.PEM,
            serialization.PrivateFormat.TraditionalOpenSSL,
            serialization.NoEncryption()
        ).decode('ASCII')
        
        ssock.close()
        
        return {
            'private_key': private_key_pem,
            'signed_csr': signed_csr,
            'proc_root': proc_root_cert,
            'proc_intermediate': proc_intermediate_cert,
        }
    
    except socket.timeout:
        print_error(f"Connection timeout - button not pressed within {BUTTON_PRESS_TIMEOUT} seconds")
        return None
    except Exception as e:
        print_error(f"LAP handshake failed: {e}")
        import traceback
        traceback.print_exc()
        return None

def save_leap_certificates(certs, processor_ipv4):
    """Save LEAP certificates to processor-specific directory"""
    print_header("Saving LEAP Certificates")
    
    # Create processor-specific certificate directory
    cert_dir = f"app/certificates/{processor_ipv4}"
    os.makedirs(cert_dir, exist_ok=True)
    print_info(f"Saving certificates to: {cert_dir}")
    
    # Define expected LEAP certificate files
    leap_cert_files = [
        ("leap_private_key.pem", certs['private_key'], "LEAP Private Key"),
        ("leap_signed_csr.pem", certs['signed_csr'], "LEAP Signed CSR"),
        ("leap_lutron_proc_root.pem", certs['proc_root'], "Processor Root Certificate"),
        ("leap_lutron_proc_intermediate.pem", certs['proc_intermediate'], "Processor Intermediate Certificate"),
    ]
    
    # Check if folder already exists with certificates
    folder_exists = os.path.exists(cert_dir) and os.path.isdir(cert_dir)
    if folder_exists:
        # List all files in the directory
        existing_files = []
        if os.path.exists(cert_dir):
            for item in os.listdir(cert_dir):
                item_path = os.path.join(cert_dir, item)
                if os.path.isfile(item_path):
                    existing_files.append(item)
        
        if existing_files:
            # Check for old LEAP certificates
            old_leap_files = [f for f in existing_files if f.startswith("leap_")]
            # Check for obsolete LAP certificates (shouldn't be in IP folder)
            obsolete_lap_files = [f for f in existing_files if f.startswith("lap_")]
            
            if old_leap_files or obsolete_lap_files:
                print_warning(f"Found existing files in {cert_dir}:")
                if old_leap_files:
                    for f in old_leap_files:
                        print_warning(f"  - {f} (will be overwritten)")
                if obsolete_lap_files:
                    for f in obsolete_lap_files:
                        print_warning(f"  - {f} (obsolete, will be removed)")
                    # Remove obsolete LAP files
                    for filename in obsolete_lap_files:
                        try:
                            filepath = os.path.join(cert_dir, filename)
                            os.remove(filepath)
                            print_info(f"Removed obsolete file: {filename}")
                        except Exception as e:
                            print_warning(f"Could not remove {filename}: {e}")
    
    # Save LEAP certificates (overwrites existing)
    for filename, content, name in leap_cert_files:
        try:
            filepath = os.path.join(cert_dir, filename)
            with open(filepath, "w") as f:
                f.write(content)
            print_success(f"{name} saved to {filepath}")
        except Exception as e:
            print_error(f"Failed to save {name}: {e}")
            return False
    
    return True

