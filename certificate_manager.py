class ProcessorDiscovery:
    """Discovers Lutron processors on the network using mDNS/Bonjour"""
    
    def __init__(self):
        self.processors = []
    
    class Listener(ServiceListener):
        def __init__(self, parent):
            self.parent = parent
        
        def add_service(self, zc: Zeroconf, type_: str, name: str) -> None:
            info = zc.get_service_info(type_, name)
            if info:
                processor = {
                    'server': info.server,
                    'ipv4': str(info._ipv4_addresses[0]) if info._ipv4_addresses else None,
                    'system': info.properties.get(b'SYSTYPE', b'').decode("ASCII"),
                    'serial': info.properties.get(b'SERNUM', b'').decode("ASCII"),
                    'mac': info.properties.get(b'MACADDR', b'').decode("ASCII"),
                    'claimed': info.properties.get(b'CLAIM_STATUS', b'').decode("ASCII"),
                    'version': info.properties.get(b'CODEVER', b'').decode("ASCII"),
                }
                if processor['ipv4']:
                    self.parent.processors.append(processor)
        
        def update_service(self, zc: Zeroconf, type_: str, name: str) -> None:
            self.add_service(zc, type_, name)
        
        def remove_service(self, zc: Zeroconf, type_: str, name: str) -> None:
            pass
    
    def discover(self, timeout=DISCOVERY_TIMEOUT):
        """Discover processors on the network"""
        print_info(f"Searching for Lutron processors ({timeout}s)...")
        
        zeroconf = Zeroconf()
        listener = self.Listener(self)
        browser = ServiceBrowser(zeroconf, "_lutron._tcp.local.", listener)
        
        try:
            time.sleep(timeout)
        finally:
            zeroconf.close()
        
        return self.processors

def display_processors(processors):
    """Display discovered processors in a formatted table"""
    if not processors:
        print_error("No Lutron processors found on the network!")
        return False
    
    print(f"\n{Colors.BOLD}Found {len(processors)} Lutron processor(s):{Colors.ENDC}\n")
    print(f"{'#':<4} {'Server':<30} {'IPv4':<16} {'System':<18} {'Serial':<12} {'Status'}")
    print("-" * 100)
    
    for idx, proc in enumerate(processors, 1):
        status = f"{Colors.OKGREEN}{proc['claimed']}{Colors.ENDC}" if proc['claimed'] == 'Claimed' else proc['claimed']
        print(f"{idx:<4} {proc['server']:<30} {proc['ipv4']:<16} {proc['system']:<18} {proc['serial']:<12} {status}")
    
    return True

def select_processor(processors):
    """Prompt user to select a processor"""
    while True:
        try:
            choice = input(f"\n{Colors.BOLD}Select processor number (1-{len(processors)}) or 'q' to quit: {Colors.ENDC}")
            
            if choice.lower() == 'q':
                print_warning("Operation cancelled by user.")
                return None
            
            idx = int(choice) - 1
            if 0 <= idx < len(processors):
                return processors[idx]
            else:
                print_error(f"Invalid selection. Please enter a number between 1 and {len(processors)}.")
        except ValueError:
            print_error("Invalid input. Please enter a number.")

