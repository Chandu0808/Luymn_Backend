def check_file(filepath, name):
    """Check if a file exists and get its info"""
    exists = os.path.isfile(filepath)
    
    if exists:
        stat = os.stat(filepath)
        size = stat.st_size
        modified = datetime.fromtimestamp(stat.st_mtime).strftime('%Y-%m-%d %H:%M:%S')
        status = f"{Colors.OKGREEN}[EXISTS]{Colors.ENDC}"
        details = f"({size:,} bytes, modified: {modified})"
    else:
        status = f"{Colors.FAIL}[MISSING]{Colors.ENDC}"
        details = ""
    
    return exists, status, details

