def verify_lap_certificates():
    """Verify LAP certificates exist"""
    print_info("Checking for LAP certificates...")
    
    required_files = [
        ("LAP Private Key", LAP_PRIVATE_KEY_FILE),
        ("LAP Signed CSR", LAP_SIGNED_CSR_FILE),
        ("LAP Lutron Root", LAP_LUTRON_ROOT_FILE),
        ("LAP Lutron Intermediate", LAP_LUTRON_INTERMEDIATE_FILE),
    ]
    
    missing = []
    for name, path in required_files:
        if not os.path.isfile(path):
            missing.append((name, path))
    
    if missing:
        print_error("Missing required LAP certificates:")
        for name, path in missing:
            print(f"  - {name}: {path}")
        print_warning("\nPlease obtain these certificates from Lutron Developer Portal:")
        print_warning("https://developer.lutron.com/")
        return False
    
    print_success("All LAP certificates found")
    return True

def create_lap_chain():
    """Combine LAP root and intermediate certificates into chain file"""
    print_info("Creating LAP certificate chain...")
    
    try:
        with open(LAP_LUTRON_CHAIN_FILE, "w") as dst_file:
            with open(LAP_LUTRON_ROOT_FILE, "r") as src_file:
                dst_file.write(src_file.read())
            with open(LAP_LUTRON_INTERMEDIATE_FILE, "r") as src_file:
                dst_file.write(src_file.read())
        
        print_success("LAP certificate chain created")
        return True
    except Exception as e:
        print_error(f"Failed to create certificate chain: {e}")
        return False

def generate_leap_keys():
    """Generate device-specific private key and CSR for LEAP"""
    print_info("Generating LEAP private key and CSR...")
    
    try:
        cert_subject = x509.Name([
            x509.NameAttribute(NameOID.COMMON_NAME, DEVICE_COMMON_NAME)
        ])
        
        private_key = rsa.generate_private_key(
            public_exponent=65537,
            key_size=2048,
            backend=default_backend()
        )
        
        csr = x509.CertificateSigningRequestBuilder().subject_name(
            cert_subject
        ).sign(private_key, hashes.SHA256(), default_backend())
        
        print_success("LEAP key pair and CSR generated")
        return private_key, csr
    
    except Exception as e:
        print_error(f"Failed to generate keys: {e}")
        return None, None

