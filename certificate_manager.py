def run_handshake():
    """Execute the complete certificate handshake workflow"""
    print_header("Lutron Processor Certificate Handshake")
    print(f"{Colors.BOLD}This will pair your application with a Lutron processor{Colors.ENDC}")
    print(f"{Colors.BOLD}using the LAP (Lutron Authentication Protocol){Colors.ENDC}\n")
    
    # Step 1: Check LAP certificates
    if not verify_lap_certificates():
        return False
    
    # Step 2: Create certificate chain
    if not create_lap_chain():
        return False
    
    # Step 3: Discover processors
    discovery = ProcessorDiscovery()
    processors = discovery.discover()
    
    if not display_processors(processors):
        return False
    
    # Step 4: Select processor
    processor = select_processor(processors)
    if not processor:
        return False
    
    print(f"\n{Colors.OKGREEN}Selected: {processor['server']} ({processor['ipv4']}){Colors.ENDC}")
    print(f"System: {processor['system']}, Serial: {processor['serial']}, MAC: {processor['mac']}\n")
    
    # Step 5: Generate LEAP keys
    private_key, csr = generate_leap_keys()
    if not private_key or not csr:
        return False
    
    # Step 6: Perform LAP handshake
    certs = perform_lap_handshake(processor, private_key, csr)
    if not certs:
        print_error("\nCertificate handshake failed!")
        return False
    
    # Step 7: Save certificates
    if not save_leap_certificates(certs, processor['ipv4']):
        print_error("\nFailed to save certificates!")
        return False
    
    # Success!
    print_header("Certificate Handshake Complete!")
    print_success("Your application is now paired with the Lutron processor")
    print_info("You can now connect to the processor using LEAP on port 8081")
    print_info(f"The certificates have been saved to app/certificates/{processor['ipv4']}/")
    print(f"\n{Colors.BOLD}Next steps:{Colors.ENDC}")
    print("  1. Restart your application to use the new certificates")
    print("  2. Your app will automatically connect to the processor")
    print()
    
    return True

def show_menu():
    """Display interactive menu"""
    print_header("Lutron Certificate Manager")
    print(f"{Colors.BOLD}Select an operation:{Colors.ENDC}\n")
    print("  1. Check certificate status")
    print("  2. Perform certificate handshake")
    print("  3. Exit")
    print()
    
    while True:
        try:
            choice = input(f"{Colors.BOLD}Enter your choice (1-3): {Colors.ENDC}")
            
            if choice == '1':
                print()
                check_certificate_status()
                print()
                return
            elif choice == '2':
                print()
                run_handshake()
                return
            elif choice == '3':
                print_info("Goodbye!")
                return
            else:
                print_error("Invalid choice. Please enter 1, 2, or 3.")
        except (KeyboardInterrupt, EOFError):
            print(f"\n{Colors.WARNING}Operation cancelled by user.{Colors.ENDC}")
            return

# ============================================================================
# MAIN ENTRY POINT
# ============================================================================

def main():
    """Main execution flow"""
    parser = argparse.ArgumentParser(
        description="Lutron Processor Certificate Manager",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python certificate_manager.py              # Interactive menu
  python certificate_manager.py check        # Check certificate status
  python certificate_manager.py handshake    # Perform handshake
        """
    )
    
    parser.add_argument(
        'action',
        nargs='?',
        choices=['check', 'handshake'],
        help='Action to perform (if not specified, shows interactive menu)'
    )
    
    args = parser.parse_args()
    
    try:
        if args.action == 'check':
            check_certificate_status()
        elif args.action == 'handshake':
            run_handshake()
        else:
            show_menu()
    
    except KeyboardInterrupt:
        print(f"\n\n{Colors.WARNING}Operation cancelled by user.{Colors.ENDC}")
        sys.exit(0)
    except Exception as e:
        print_error(f"\nUnexpected error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)

if __name__ == "__main__":
    main()

